#             NO cls/def/main.tex/data, so the COMPILE PATH IS UNAFFECTED. Therefore the integration layer has NO RED
#             drivers (the RED is all unit/doc-level: artifacts absent, README still ZZU ŌĆö see test-story-4.4-unit.sh).
#             The 4 integration tests are GREEN GATES/GUARDS that prove "docs-only did not regress the compile":
#               I01 (gate) default compile rc0/errors0/warningsŌēżbaseline+3/pagesŌēź40,
#               I02 (AC-10) default .log self-check structurally identical to 7e0d103 (markers + 8 dims + 4.2 sections
#                   + debug section ABSENT on default ŌĆö 4.4 consumer, no cls edit),
#               I03 (TC-E4-31) fresh-user clean-compile: rm intermediates + latexmk -xelatex main exit 0 (NO make
#                   dependency ŌĆö 4.3 Windows-no-make precedent; README documents latexmk as the primary command),
#               I04 (guard) 4.3 Makefile calibrate + debug-check RECIPES intact (4.4 documents these tools, does not
