#   --run    Remove SKIP marker (for green-phase verification)
#
# Priority: P0 (I01 compile gate + I02 self-check intact + I03 fresh-user compile ŌĆö the no-regression pillars) /
#           P1 (I04 4.3-tools recipe intact)
# Linked ACs: AC-8 (fresh-user compiles via README only), AC-10 (no regression ŌĆö self-check byte-identical, 4.3 tools)
# Linked Risk: R-28 (README fresh-user compile ŌĆö I03), R-27/R-25 (checklist ŌĆö unit-level, not integration)
